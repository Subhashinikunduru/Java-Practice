package com.javaintro;

public class Ascii {
	static char a = 'A';
	static char b = 'B';
	static char c = 'C';

	public static void main(String[] args) {
		System.out.println("Ascii value of" + a + "is:" + (int) a);
		System.out.println("Ascii value of" + b + "is:" + (int) b);
		System.out.println("Ascii value of" + c + "is:" + (int) c);

	}

}
